# Минимальное подключение к Oracle DB из Jupyter Notebook (стандартный Python)
# Библиотека oracledb установлена в стандартном окружении Python (не Anaconda)
# Установка: pip install oracledb (в системном Python)
# Тестировано: Python 3.12+, Oracle 19c/23c, thin mode (без Oracle Client)

import oracledb
# Импорт библиотеки oracledb для работы с базами данных Oracle
# Эта библиотека позволяет подключаться к Oracle без установки Oracle Client (thin mode)

import getpass
# Импорт модуля getpass для безопасного ввода пароля (скрывает символы при вводе)

from contextlib import contextmanager
# Импорт декоратора contextmanager для создания контекстных менеджеров
# Контекстные менеджеры обеспечивают безопасное управление ресурсами (подключениями)

import sys
# Импорт модуля sys для доступа к системным параметрам и путям интерпретатора

print(f"Python: {sys.executable}")
# Вывод полного пути к исполняемому файлу Python
# Позволяет убедиться, что используется системный Python, а не conda окружение

print(f"oracledb версия: {oracledb.__version__}")
# Вывод версии установленной библиотеки oracledb
# Помогает проверить совместимость и факт успешной установки

print("✓ Библиотека найдена в стандартном Python окружении")
# Информационное сообщение об успешном импорте библиотеки


@contextmanager
# Декоратор, превращающий функцию в контекстный менеджер
# Позволяет использовать функцию в конструкции 'with' для автоматического управления ресурсами
def get_oracle_connection(user: str, dsn: str):
    # Определение функции get_oracle_connection с двумя строковыми параметрами
    # user: имя пользователя базы данных
    # dsn: строка подключения (Data Source Name) в формате host:port/service_name
    """Контекстный менеджер для безопасного подключения."""
    # Документирующая строка (docstring), описывающая назначение функции
    
    password = getpass.getpass("Oracle пароль: ")
    # Запрос пароля у пользователя с скрытым отображением вводимых символов
    # Повышает безопасность, предотвращая отображение пароля в консоли
    
    conn = None
    # Инициализация переменной conn значением None
    # Необходима для корректной работы блока finally даже если подключение не удастся
    
    try:
        # Начало блока обработки исключений (попытки выполнения кода)
        conn = oracledb.connect(user=user, password=password, dsn=dsn)
        # Установление подключения к базе данных Oracle
        # oracledb.connect: метод подключения с переданными учетными данными
        # user: имя пользователя, password: пароль, dsn: строка подключения
        print("✓ Подключение успешно")
        # Сообщение об успешном подключении
        
        yield conn
        # Ключевое слово yield возвращает объект подключения в блок with
        # После завершения блока with выполнение продолжится с этой строки
        
    except oracledb.Error as e:
        # Блок перехвата исключений типа oracledb.Error (ошибки подключения/запросов)
        # e: объект исключения, содержащий информацию об ошибке
        print(f"✗ Ошибка подключения: {e}")
        # Вывод сообщения об ошибке с деталями
        
        raise
        # Повторное выбрасывание исключения для прерывания выполнения
        # Позволяет вызывающему коду обработать ошибку
        
    finally:
        # Блок, выполняющийся всегда (независимо от наличия ошибок)
        # Гарантирует освобождение ресурсов
        if conn:
            # Проверка: если подключение было установлено (conn не None)
            conn.close()
            # Закрытие соединения с базой данных
            # Освобождает ресурсы на стороне клиента и сервера
            
            print("✓ Соединение закрыто")
            # Сообщение об успешном закрытии соединения


# === НАСТРОЙКА ===
# Разделитель для визуального отделения конфигурационной секции

USERNAME = "your_username"
# Имя пользователя базы данных Oracle
# ЧТО НУЖНО УКАЗАТЬ: замените "your_username" на реальное имя пользователя
# Примеры: "hr", "scott", "system" или ваше корпоративное имя пользователя

DSN = "localhost:1521/XEPDB1"
# Строка подключения к базе данных (Data Source Name)
# ЧТО НУЖНО УКАЗАТЬ: замените на ваши параметры подключения
# Формат: "хост:порт/имя_сервиса" или "хост:порт:SID"
# Примеры:
#   - "192.168.1.100:1521/ORCL" (удаленный сервер)
#   - "db.example.com:1521/PDB1" (доменное имя)
#   - "localhost:1521/XEPDB1" (локальная база, сервис)
#   - "localhost:1521:XE" (локальная база, SID)


# === ТЕСТИРОВАНИЕ ===
# Разделитель для визуального отделения тестовой секции

print("\n=== ТЕСТ ПОДКЛЮЧЕНИЯ ===")
# Вывод заголовка тестовой секции
# \n добавляет пустую строку перед заголовком для улучшения читаемости

with get_oracle_connection(USERNAME, DSN) as conn:
    # Использование контекстного менеджера для подключения к БД
    # conn: объект подключения, возвращенный через yield
    # Автоматически закроет соединение после выхода из блока with
    
    cursor = conn.cursor()
    # Создание курсора для выполнения SQL-запросов
    # Курсор позволяет выполнять запросы и получать результаты
    
    # 1. Базовый тест DUAL
    # DUAL - специальная однострочная таблица в Oracle для тестовых запросов
    cursor.execute("SELECT 'OK' as status FROM dual")
    # Выполнение простого SQL-запроса к таблице DUAL
    # Возвращает строку со значением 'OK' в колонке status
    
    print("✓ DUAL тест:", cursor.fetchone()[0])
    # Получение и вывод первой строки результата
    # fetchone(): возвращает первую строку как кортеж
    # [0]: извлечение первого элемента кортежа (значение 'OK')
    
    # 2. Информация о БД
    # Запрос версии Oracle из системного представления v$version
    cursor.execute("SELECT banner FROM v$version WHERE rownum = 1")
    # Выполнение запроса к системному представлению v$version
    # banner: полное описание версии и сборки Oracle
    # rownum = 1: ограничение одной строкой (первая попавшаяся)
    
    print("✓ Oracle версия:", cursor.fetchone()[0][:60] + "...")
    # Получение, обрезка и вывод информации о версии
    # [:60]: обрезка строки до 60 символов для компактности
    # + "...": добавление многоточия для обозначения обрезки
    
    # 3. Счетчик объектов (проверка доступа)
    # Подсчет всех объектов в текущей схеме пользователя
    cursor.execute("SELECT COUNT(*) as objects FROM user_objects")
    # Выполнение запроса подсчета объектов в user_objects
    # user_objects: системное представление со всеми объектами текущей схемы
    # COUNT(*): агрегатная функция подсчета количества строк
    
    print("✓ Объектов в схеме:", cursor.fetchone()[0])
    # Получение и вывод количества объектов
    # Демонстрирует, что у пользователя есть права на чтение метаданных

print("\n🎉 ГОТОВО! Код протестирован и готов к публикации")
# Финальное сообщение об успешном завершении всех тестов
# \n добавляет пустую строку для разделения от предыдущего вывода

